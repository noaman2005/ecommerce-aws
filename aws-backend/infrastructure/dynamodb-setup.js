async function createTables() {
  try {
    // List existing tables
    const listCommand = new ListTablesCommand({});
    const existingTables = await client.send(listCommand);
    
    console.log('Existing tables:', existingTables.TableNames);

    for (const tableConfig of tables) {
      if (existingTables.TableNames.includes(tableConfig.TableName)) {
        console.log(`Table ${tableConfig.TableName} already exists, skipping...`);
        continue;
      }

      console.log(`Creating table: ${tableConfig.TableName}...`);
      const command = new CreateTableCommand(tableConfig);
      await client.send(command);
      console.log(`✓ Table ${tableConfig.TableName} created successfully`);
    }

    console.log('\n✓ All tables created successfully!');
  } catch (error) {
    console.error('Error creating tables:', error);
    process.exit(1);
  }
}

